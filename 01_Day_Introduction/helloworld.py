print(1 + 1)  # addition(+)
print(1 - 1)  # subtraction(-) 
print(1 * 1)  # multiplication(*)
print(1 / 1)  # division(/)
print(1 ** 1) # exponential(**)
print(1 % 1)  # modulus(%)
print(1 // 1) # Floor division operator(//)

# Checking data types

print(type(10))                     # Int
print(type(3.14))                   # Float
print(type(1 + 3j))                 # Complex
print(type('Alberth'))              # String
print(type([1,2,3]))                # List
print(type({"name" : "Alberth"}))   # Dictionary  
print(type({9.8, 3.14, 2.7}))       # Set
print(type((9.8, 3.14, 2.7)))       # Tuple